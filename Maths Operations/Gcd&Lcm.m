%% For hcf (Highest Common Factor) 
gcd(12,15)
% computers greatest common divisor
a = floor(rand(2,2)*10);
b = floor(rand(2,2)*10);
% perform element wise gcd so matrix must be of same size
gcd(a,b)
% Matrix with an particular element 
a = [10 20;30 40];
gcd(a,10);

